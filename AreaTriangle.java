public class AreaTriangle {
    public static void main(String[] args) {
        Double sideA = 4.0;
        Double sideB = 5.0;
        Double sideC = 3.0;
        Double triangleArea= (sideA + sideB + sideC) / 2;
        System.out.println("The area is: " + triangleArea);
    }
}
